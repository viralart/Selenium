public class loops {
    public static void main(String args[]){
        int i=1;
        while(i<=10){
            System.out.println(i);
            i++;
        }

        int j=10;
        while(j>0){
            System.out.println(j);
            j--;
        }

        int k=1;
        do{
            System.out.println(k);
            k++;
        }while(k<=10);

        for(int x=1;x<=10;x++){
            System.out.println(x);
        }
        for(int x=10;x>0;x--){
            System.out.println(x);
        }

    }
}
